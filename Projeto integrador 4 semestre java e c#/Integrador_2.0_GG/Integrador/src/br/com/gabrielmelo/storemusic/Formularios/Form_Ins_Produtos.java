     public void preencherCampo(int id){
        ProdutosTO c = new ProdutosTO();
         
        c.setCod_prod(id);
        ListIterator<ProdutosTO> lstiP = new ProdutosBO().getDadosProd(c).listIterator();
                    while(lstiP.hasNext())
        {
            c = lstiP.next();
            
            txtTipo.setText(c.getTipo());
            txtNome.setText(c.getNome());
            txtUnidade.setText(String.valueOf(c.getUnidade()));
            //txt.setText(c.getData());
            txtValor.setText(String.valueOf(c.getValor()));
            txtBarras.setText(String.valueOf(c.getCod_barras()));
            txtValor_custo.setText(String.valueOf(c.getValor_custo()));
        } 
    }
